public class Employee {
  private String name;
  private int id;
  private int salary;

  // Get name
  public String getName() {
    return name;
  }

  // Set name
  public void setName(String name) {
    this.name = name;
  }

  // Get id
  public int getId() {
    return id;
  }

  // Set id
  public void setId(int idx) {
    this.id = idx;
  }

  // Get salary
  public int getSalary() {
    return salary;
  }

  // Set salary
  public void setSalary(int salary) {
    this.salary = salary;
  }
}
