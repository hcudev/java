public class IfElse {
  public static void main(String[] args) {
    int number = 5;
     // If Else Statement
    if (number > 5) {
      System.out.printf("The integer %d is greater than 5", number);
    } else {
      System.out.printf("The integer %d is not greater than 5", number);
    }
  }
}

